#include "minishell.h"

int	check_after_d(char c)
{
	if (c == 32
		|| (c >= 9 && c <= 13)
		|| c == '|'
		|| c == '>'
		|| c == '<'
		|| c == '"'
		|| c == '='
		|| c == '$'
		|| c == '\0')
		return (0);
	return (1);
}

int	end_point(char c, int *sign)
{
	if (*sign == 0)
	{
		if (c == 32
			|| (c >= 9 && c <= 13)
			|| c == '|'
			|| c == '>'
			|| c == '<'
			|| c == '&'
			|| c == ')'
			|| c == '('
			|| c == '\0')
			return (0);
	}
	return (1);
}

int	c_after_add(char c)
{
	if (c != '$' && c != '\'' && c != '"')
		return (1);
	return (0);
}
