#include "../minishell.h"

int	end_point(char c, int *sign)
{
	if (*sign == 0)
	{
		if (c == 32
			|| (c >= 9 && c <= 13)
			|| c == '|'
			|| c == '>'
			|| c == '<'
			|| c == '\0')
			return (0);
	}
	return (1);
}
